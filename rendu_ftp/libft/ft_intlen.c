#include "libft.h"

int	ft_intlen(int c)
{
	int		len;

	len = 0;
	if (c == 0)
		c = c + 1;
	while (c >= 10)
	{
		c = c / 10;
		len++;
	}
	if (c > 0)
		len++;
	return (len);
}
