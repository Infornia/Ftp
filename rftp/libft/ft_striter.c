#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		while (i < (int)ft_strlen(s))
		{
			f(&s[i]);
			i++;
		}
	}
}
